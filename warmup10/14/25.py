# Convert the string "Dino" to a boolean.
# Since "Dino" is not an empty string, bool("Dino") will be True.
dino_as_bool = bool("Dino")

# Then, convert the boolean (True) to an integer.
# The integer equivalent of True is 1.
dino_as_int = int(dino_as_bool)

# Print the result to verify
print(dino_as_int)

